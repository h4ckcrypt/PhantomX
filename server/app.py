@app.route("/track", methods=["POST"])
def track():
    data = request.get_json(silent=True)
    if not data:
        try:
            data = json.loads(request.data.decode("utf-8"))
        except Exception:
            data = {}

    session_id = data.get("session_id")
    event      = data.get("event")
    extra      = data.get("data", {})
    cid        = data.get("campaign_id") or None
    uid        = data.get("user_id")     or None

    log_event(session_id, event, extra, cid=cid, uid=uid)

    if cid and uid:
        if event in ("keypress", "input_change", "field_focus"):
            record_flow_step(cid, uid, "interaction", session_id=session_id)
        elif event == "form_submit":
            record_flow_step(cid, uid, "compromised", session_id=session_id)

    return jsonify({"status": "ok"})


