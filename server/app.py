def list_phishing_templates() -> list[str]:

    result = []
    if not os.path.isdir(TEMPLATES_DIR):
        return result
    for name in sorted(os.listdir(TEMPLATES_DIR)):
        if name == "admin":
            continue
        t_path = os.path.join(TEMPLATES_DIR, name)
        if os.path.isdir(t_path) and os.path.exists(os.path.join(t_path, "index.html")):
            result.append(name)
    return result


def resolve_template(name: str) -> str:
    
    safe_name = os.path.basename(name) if name else DEFAULT_TEMPLATE
    t_path    = os.path.join(TEMPLATES_DIR, safe_name)
    if os.path.isdir(t_path) and os.path.exists(os.path.join(t_path, "index.html")):
        return safe_name
    return DEFAULT_TEMPLATE


def serve_template_page(template_name: str,
                        session_id: str,
                        cid: str = "",
                        uid: str = "") -> str:
    """
    Load index.html from the given template, replace %%MARKERS%%,
    and return the rendered HTML string.

    MARKERS (double-percent — no Jinja2 collision):
        %%SESSION_ID%%    → session UUID
        %%CAMPAIGN_ID%%   → campaign id or ""
        %%USER_ID%%       → user id or ""
        %%TEMPLATE_NAME%% → template folder name (for asset URLs)

    Each template's index.html uses:
        /t/%%TEMPLATE_NAME%%/assets/js/tracker.js
    which resolves to e.g.:
        /t/login_basic/assets/js/tracker.js
    """
    t_path    = os.path.join(TEMPLATES_DIR, template_name)
    index_path = os.path.join(t_path, "index.html")

    with open(index_path, "r", encoding="utf-8") as f:
        html = f.read()

    html = html.replace("%%SESSION_ID%%",    session_id)
    html = html.replace("%%CAMPAIGN_ID%%",   cid)
    html = html.replace("%%USER_ID%%",       uid)
    html = html.replace("%%TEMPLATE_NAME%%", template_name)

    html = html.replace("{{SESSION_ID}}",    session_id)
    html = html.replace("{{CAMPAIGN_ID}}",   cid)
    html = html.replace("{{USER_ID}}",       uid)
    html = html.replace("{{TEMPLATE_NAME}}", template_name)

    
    return html

def _read_log() -> dict:
    try:
        with open(LOG_FILE) as f:
            data = json.load(f)
        return data if isinstance(data, dict) else {}
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def _write_log(data: dict) -> None:
    os.makedirs(os.path.dirname(LOG_FILE), exist_ok=True)
    tmp = LOG_FILE + ".tmp"
    with open(tmp, "w") as f:
        json.dump(data, f, indent=2)
    os.replace(tmp, LOG_FILE)


def _unwrap_session(session_data):
    if isinstance(session_data, dict):
        return session_data.get("events", []), session_data.get("status", "active")
    if isinstance(session_data, list):
        return session_data, "unknown"
    return [], "unknown"


def _live_status(events: list, stored: str) -> str:
    if not events:
        return stored
    last_ev = events[-1].get("event", "")
    last_ts = events[-1].get("timestamp", 0)
    if last_ev == "page_close" or stored == "closed":
        return "closed"
    if time.time() - last_ts > 30:
        return "idle"
    return "active"


def _ai_blend(heuristic: float, ai_label: str) -> float:
    modifier = {"High Risk": 10, "Suspicious": 5, "Safe": -3}.get(ai_label, 0)
    return max(0.0, min(100.0, heuristic + modifier))


def _build_session_row(sid: str, sdata) -> dict | None:
    events, stored = _unwrap_session(sdata)
    if not events:
        return None
    h        = calculate_score(events)
    ai_label = predict_user(events)
    score    = _ai_blend(h, ai_label)
    return {
        "id":            sid,
        "id_short":      sid[:8],
        "score":         round(score, 1),
        "category":      classify_user(score),
        "status":        _live_status(events, stored),
        "events":        len(events),
        "ai_prediction": ai_label,
        "campaign_id":   sdata.get("campaign_id") if isinstance(sdata, dict) else None,
        "user_id":       sdata.get("user_id")     if isinstance(sdata, dict) else None,
    }


