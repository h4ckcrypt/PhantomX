@app.route("/api/campaigns/<cid>/targets/bulk", methods=["POST"])
@require_admin
def api_add_targets_bulk(cid):
    content_type = request.content_type or ""
    if "application/json" in content_type:
        body    = request.get_json(silent=True) or {}
        entries = body.get("targets", [])
    else:
        lines   = request.data.decode("utf-8").strip().splitlines()
        entries = []
        for line in lines:
            parts = [p.strip() for p in line.split(",")]
            if parts and parts[0]:
                entries.append({"email": parts[0], "name": parts[1] if len(parts) > 1 else ""})

    created = add_targets_bulk(cid, entries)
    result  = [{**t, "tracking_url": _tracking_url(t["link_token"])} for t in created]
    return jsonify({"created": len(result), "targets": result}), 201


