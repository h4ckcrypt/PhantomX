@app.route("/api/session/<session_id>/analysis")
@require_admin
def api_session_analysis(session_id):
    data        = _read_log()
    sdata       = data.get(session_id)
    if sdata is None:
        return jsonify({"error": "Session not found"}), 404

    events, stored_status = _unwrap_session(sdata)
    if not events:
        return jsonify({"error": "No events"}), 404

    status = _live_status(events, stored_status)

    clicks          = sum(1 for e in events if e.get("event") == "click")
    keypresses      = sum(1 for e in events if e.get("event") == "keypress")
    inputs_ev       = sum(1 for e in events if e.get("event") in ("input", "input_change"))
    tab_switches    = sum(1 for e in events if e.get("event") == "tab_hidden")
    mousemoves      = sum(1 for e in events if e.get("event") == "mousemove")
    idle_events     = [e for e in events if e.get("event") == "idle"]
    form_submit     = any(e.get("event") == "form_submit" for e in events)
    form_attempt    = any(e.get("event") == "form_submit_attempt" for e in events)
    copy_events     = sum(1 for e in events if e.get("event") == "copy")
    paste_events    = sum(1 for e in events if e.get("event") == "paste")
    autofill_ev     = [e for e in events if e.get("event") == "autofill"]
    field_focus_ev  = [e for e in events if e.get("event") == "field_focus"]

    duration = 0
    if len(events) >= 2:
        duration = events[-1].get("timestamp", 0) - events[0].get("timestamp", 0)
    max_idle = max((e.get("data", {}).get("duration", 0) for e in idle_events), default=0)

    h        = calculate_score(events)
    ai_label = predict_user(events)
    score    = _ai_blend(h, ai_label)
    category = classify_user(score)

    has_click = clicks > 0
    has_type  = (keypresses + inputs_ev) > 0

    if not has_click and not has_type:
        eng_val, eng_why = "None",    "No clicks or keystrokes — possible bot or bounce"
    elif has_click and not has_type:
        eng_val, eng_why = "Low",     f"Clicked {clicks}× but never typed"
    elif has_click and has_type:
        eng_val, eng_why = "High",    f"Clicked {clicks}× and typed {keypresses+inputs_ev} keypress(es)"
    else:
        eng_val, eng_why = "Partial", "Typed without clicking"

    if mousemoves == 0:
        m_val, m_why = "None",   "No mouse movement"
    elif mousemoves < 10:
        m_val, m_why = "Low",    f"Only {mousemoves} movement(s)"
    elif mousemoves < 40:
        m_val, m_why = "Normal", "Continuous non-linear movement — human browsing"
    else:
        m_val, m_why = "High",   f"Extensive activity ({mousemoves} movements)"

    if keypresses == 0 and inputs_ev == 0:
        t_val, t_why = "None",    "No keypress or input events"
    elif keypresses < 5:
        t_val, t_why = "Minimal", f"Only {keypresses} keypress(es)"
    else:
        t_val, t_why = "Active",  f"{keypresses} keypress(es) — manually typed"

    n_focus = len(field_focus_ev)
    fm      = [e.get("data", {}).get("method", "?") for e in field_focus_ev]
    if n_focus == 0:
        fi_val, fi_why = "None", "No input field interactions"
    else:
        fi_val = "Active"
        fi_why = f"{n_focus} focus event(s): {fm.count('click')} click, {fm.count('tab')} tab"

    if tab_switches == 0:
        tab_val, tab_why = "None",     "Stayed on page"
    elif tab_switches <= 2:
        tab_val, tab_why = "Moderate", f"Switched tabs {tab_switches}×"
    else:
        tab_val, tab_why = "High",     f"Switched tabs {tab_switches}× — frequent"

    if not idle_events:
        idle_val, idle_why = "None",    "No idle periods"
    elif max_idle < 10:
        idle_val, idle_why = "Brief",   f"{round(max_idle,1)}s inactivity"
    elif max_idle < 30:
        idle_val, idle_why = "Present", f"{round(max_idle,1)}s pause"
    else:
        idle_val, idle_why = "Long",    f"{round(max_idle,1)}s extended idle"

    cp = copy_events + paste_events
    if cp == 0:
        cp_val, cp_why = "None", "No copy or paste activity"
    else:
        parts = []
        if copy_events:  parts.append(f"{copy_events} copy")
        if paste_events: parts.append(f"{paste_events} paste")
        cp_val = "Detected"
        cp_why = f"{' + '.join(parts)} event(s)"

    n_af = len(autofill_ev)
    if n_af == 0:
        af_val, af_why = "None",    "Fields typed manually"
    elif keypresses < 3:
        af_val, af_why = "Full",    f"{n_af} field(s) via autofill"
    else:
        af_val, af_why = "Partial", f"{n_af} autofill + manual typing"

    if form_submit:
        sub_val, sub_why = "Yes",       "Form submitted — credentials captured"
    elif form_attempt:
        sub_val, sub_why = "Attempted", "Tried to submit, invalid email"
    else:
        sub_val, sub_why = "No",        "Did not submit"

    return jsonify({
        "session_id":    session_id,
        "status":        status,
        "risk_score":    round(score, 1),
        "risk_category": category,
        "ai_prediction": ai_label,
        "duration_s":    round(duration, 1),
        "event_count":   len(events),
        "campaign_id":   sdata.get("campaign_id") if isinstance(sdata, dict) else None,
        "user_id":       sdata.get("user_id")     if isinstance(sdata, dict) else None,
        "behaviours": [
            {"label": "Engagement",       "value": eng_val,  "reason": eng_why,  "icon": "🎯"},
            {"label": "Mouse Activity",    "value": m_val,    "reason": m_why,    "icon": "🖱️"},
            {"label": "Typing",            "value": t_val,    "reason": t_why,    "icon": "⌨️"},
            {"label": "Field Interaction", "value": fi_val,   "reason": fi_why,   "icon": "🖊️"},
            {"label": "Autofill",          "value": af_val,   "reason": af_why,   "icon": "🤖"},
            {"label": "Tab Behaviour",     "value": tab_val,  "reason": tab_why,  "icon": "🗂️"},
            {"label": "Idle Periods",      "value": idle_val, "reason": idle_why, "icon": "⏱️"},
            {"label": "Copy-Paste",        "value": cp_val,   "reason": cp_why,   "icon": "📋"},
            {"label": "Form Submitted",    "value": sub_val,  "reason": sub_why,  "icon": "📨"},
        ],
    })



